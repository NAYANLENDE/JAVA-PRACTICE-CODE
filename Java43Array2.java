import java.util.Scanner;
class Java43Array2 {
      public static void main(String[] args) {
    	Scanner  sc =new Scanner(System.in);
    	int arr[]=new int[6];
    	System.out.println("Enter the array elements: ");
    	for (int i=0;i<6; ++i) {
    		arr[i]=sc.nextInt();
    	}
    	System.out.println("The array elements are: ");
    	for (int i=0;i<6;++i) {
    		System.out.println(arr[i]);
    	}
     }
}
    	
		

	


